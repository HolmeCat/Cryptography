	int getLeft4Bit(int a){ //一个16进制数的左4位
		int left = a & 0x000000f0;
		return left >> 4;
	}
	int getRight4Bit(int a){ //一个16进制数的右4位
		return a & 0x0000000f;
	}
	int getNumFromSBox(int a, const int s[][16]){ //从s盒里面取数据
		int row = getLeft4Bit(a);
		int col = getRight4Bit(a);
		return s[row][col];
	}
	int aesMultiple(int a, int le){ //AES乘法,GF(2^8)上乘法
		int thr = le & 0x8;
		int sec = le & 0x4;
		int fir = le & 0x2;
		int fir_mod = le % 2;
		int result = 0;
		if (thr){ //第四位
			int b = a;
			for (int i = 1; i <= 3; ++i) {
				b = b << 1;
				if (b >= 256)
					b = b ^ 0x11b;
			}
			b = b % 256;
			result = result ^ b;
		}
		if(sec){
			int b = a;
			for (int i = 1;i <= 2; ++i) {
				b = b << 1;
				if (b >= 256)
					b = b ^ 0x11b;
			}
			b = b % 256;
			result = result ^ b;
		}
		if(fir){
			int b = a << 1;
			if (b >= 256)
				b = b ^ 0x11b;
			b = b % 256;
			result = result ^ b;
		}
		if(fir_mod)
			result = result ^ a;
		return  result;
	}
